import ahpy

# ── STEP 1: AHP BETAS ─────────────────────────────────────
comparisons = {
    ('duration', 'avg_defects')    : 3,
    ('duration', 'total_defects')  : 5,
    ('avg_defects', 'total_defects'): 3,
}
criteria = ahpy.Compare('Criteria', comparisons, precision=4)
beta_1 = criteria.target_weights['duration']   # cost weight
beta_2 = criteria.target_weights['avg_defects'] # duration weight
beta_3 = criteria.target_weights['total_defects'] # risk weight

print(f"β1 (cost)     = {beta_1:.4f}")
print(f"β2 (duration) = {beta_2:.4f}")
print(f"β3 (risk)     = {beta_3:.4f}")
print(f"CR = {criteria.consistency_ratio:.4f}")

# ── STEP 2: EMPLOYEE RATES (from experience level) ────────
rate_map = {
    'Intern / Trainee'   : 10,
    'Junior Developer'   : 20,
    'Mid-Level Developer': 30,
    'Senior Developer'   : 40,
    'Lead Engineer'      : 50,
}

