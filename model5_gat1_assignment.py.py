def z_from_train(all_arr, train_idx):
    tr = all_arr[train_idx]
    mu = tr.mean()
    sd = tr.std()
    if sd < 1e-8:
        return np.zeros_like(all_arr, dtype=np.float32), float(mu), float(sd)
    return ((all_arr - mu) / sd).astype(np.float32), float(mu), float(sd)

