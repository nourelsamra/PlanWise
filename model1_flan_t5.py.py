def sr_id_to_int(sr_id):
    if sr_id is None: return None
    s = str(sr_id).strip()
    if not s: return None
    m = re.search(r'(\d+)\D+(\d+)\s*$', s)
    if m:
        return int(m.group(1)) * 100 + int(m.group(2))
    digits = re.findall(r'\d+', s)
    return int("".join(digits)) if digits else None

