def generate_raw(br_json_dict):
    # ✅ FIX: prompt demands EXACTLY 2 SRs + more tokens
    prompt = (
        "Return ONLY valid JSON with EXACTLY 2 system requirements.\n"
        "Output must match this schema exactly:\n"
        '{"business_requirement":{"br_id":"","text":"","task_type":"","task_subtype":""},'
        '"system_requirements":['
        '{"sr_id":"","text":"","required_experience":"","uncertainty":0.0},'
        '{"sr_id":"","text":"","required_experience":"","uncertainty":0.0}'
        ']}\n\n'
        "INPUT:\n"
        + json.dumps({"business_requirement": br_json_dict}, ensure_ascii=False)
    )
    inputs = tokenizer(
        prompt,
        return_tensors="pt",
        truncation=True,
        max_length=512
    ).to(model.device)

    out = model.generate(
        **inputs,
        max_new_tokens=600,  # ✅ FIX: increased from 450
        num_beams=4
    )
    return tokenizer.decode(out[0], skip_special_tokens=True)


def _extract_json_array_after_key(raw: str, key: str) -> str:
    m = re.search(rf'"{re.escape(key)}"\s*:\s*\[', raw)
    if not m:
        return ""
    start = m.end() - 1
    i, depth, in_str, esc = start, 0, False, False
    while i < len(raw):
        ch = raw[i]
        if in_str:
            if esc:               esc = False
            elif ch == '\\':      esc = True
            elif ch == '"':       in_str = False
        else:
            if   ch == '"':       in_str = True
            elif ch == '[':       depth += 1
            elif ch == ']':
                depth -= 1
                if depth == 0:    return raw[start:i+1]
        i += 1
    return ""


def parse_model_output(raw_text: str):
    raw = raw_text.strip()

    def grab_str(key):
        m = re.search(rf'"{key}"\s*:\s*"([^"]*)"', raw)
        return m.group(1) if m else ""

    br = {
        "br_id":        grab_str("br_id"),
        "text":         grab_str("text"),
        "task_type":    grab_str("task_type"),
        "task_subtype": grab_str("task_subtype"),
    }

    # Try robust bracket-balanced extraction first
    sr_array_text = _extract_json_array_after_key(raw, "system_requirements")
    srs = []

    if sr_array_text:
        try:
            sr_list = json.loads(sr_array_text)
            for item in (sr_list if isinstance(sr_list, list) else []):
                if not isinstance(item, dict):
                    continue
                srs.append({
                    "sr_id":               str(item.get("sr_id", "")),
                    "text":                str(item.get("text", "")),
                    "required_experience": str(item.get("required_experience", "")),
                    "uncertainty":         float(item.get("uncertainty", 0.0)) if item.get("uncertainty", "") != "" else 0.0
                })
        except Exception:
            # Fallback: regex extraction
            sr_ids   = re.findall(r'"sr_id"\s*:\s*"((?:\\.|[^"\\])*)"',               sr_array_text, re.S)
            sr_texts = re.findall(r'"text"\s*:\s*"((?:\\.|[^"\\])*)"',                sr_array_text, re.S)
            req_exps = re.findall(r'"required_experience"\s*:\s*"((?:\\.|[^"\\])*)"', sr_array_text, re.S)
            uncs     = re.findall(r'"uncertainty"\s*:\s*([0-9]*\.?[0-9]+)',            sr_array_text)
            n = max(len(sr_ids), len(sr_texts), len(req_exps), len(uncs), 0)
            for i in range(n):
                srs.append({
                    "sr_id":               sr_ids[i]   if i < len(sr_ids)   else "",
                    "text":                sr_texts[i]  if i < len(sr_texts) else "",
                    "required_experience": req_exps[i]  if i < len(req_exps) else "",
                    "uncertainty":         float(uncs[i]) if i < len(uncs)  else 0.0
                })

    return {"business_requirement": br, "system_requirements": srs}

