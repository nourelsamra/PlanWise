class GATLayer(tf.keras.layers.Layer):

    def __init__(self, out_dim, num_heads=4, concat=True, negative_slope=0.2, **kwargs):
        super().__init__(**kwargs)
        self.out_dim        = out_dim
        self.num_heads      = num_heads
        self.concat         = concat
        self.negative_slope = negative_slope

    def build(self, input_shape):
        in_dim = input_shape[0][-1]
        self.W = [
            self.add_weight(shape=(in_dim, self.out_dim),
                            initializer="glorot_uniform", trainable=True)
            for _ in range(self.num_heads)
        ]
        self.a = self.add_weight(shape=(self.num_heads, 2 * self.out_dim),
                                 initializer="glorot_uniform", trainable=True)

    def call(self, inputs):
        X        = tf.cast(inputs[0], tf.float32)
        edge_idx = tf.cast(inputs[1], tf.int64)
        src, dst = edge_idx[0], edge_idx[1]
        N_nodes  = tf.shape(X)[0]
        outputs  = []

        for h in range(self.num_heads):
            Wh      = tf.matmul(X, self.W[h])
            Wh_src  = tf.gather(Wh, src)
            Wh_dst  = tf.gather(Wh, dst)
            e       = tf.nn.leaky_relu(
                          tf.reduce_sum(
                              tf.concat([Wh_dst, Wh_src], axis=1) * self.a[h],
                              axis=1),
                          alpha=self.negative_slope)
            alpha   = tf.exp(e)
            denom   = tf.math.unsorted_segment_sum(alpha, dst, num_segments=N_nodes)
            alpha_n = alpha / (tf.gather(denom, dst) + 1e-9)
            out     = tf.math.unsorted_segment_sum(
                          tf.expand_dims(alpha_n, 1) * Wh_src,
                          dst, num_segments=N_nodes)
            outputs.append(out)

        if self.concat:
            return tf.concat(outputs, axis=1)
        else:
            return tf.reduce_mean(tf.stack(outputs, axis=0), axis=0)

class GATModel(tf.keras.Model):
    def __init__(self, hidden_dim=64, num_heads=4, out_dim=32):
        super().__init__()
        self.gat1   = GATLayer(hidden_dim, num_heads=num_heads, concat=True)
        self.gat2   = GATLayer(out_dim, num_heads=2, concat=False)
        self.linear = tf.keras.layers.Dense(1, activation='sigmoid')


    def call(self, inputs, training=False):
        X, edge_idx = inputs
        h1  = tf.nn.elu(self.gat1([X, edge_idx]))
        h2  = tf.nn.elu(self.gat2([h1, edge_idx]))
        out = self.linear(h2)
        return out

