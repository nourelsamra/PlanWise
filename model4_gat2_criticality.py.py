def masked_mse(y_true, y_pred, mask):
    mask   = tf.cast(mask, tf.float32)
    error  = tf.square(y_true - y_pred) * tf.expand_dims(mask, 1)
    return tf.reduce_sum(error) / (tf.reduce_sum(mask) + 1e-8)

@tf.function
def train_step():
    with tf.GradientTape() as tape:
        y_pred = model([X_tf, edge_idx_tf], training=True)
        loss   = masked_mse(y_tf, y_pred, train_mask_tf)
    grads = tape.gradient(loss, model.trainable_variables)
    optimizer.apply_gradients(zip(grads, model.trainable_variables))
    return loss

@tf.function
def eval_step(mask):
    y_pred = model([X_tf, edge_idx_tf], training=False)
    return masked_mse(y_tf, y_pred, mask)

EPOCHS       = 700
best_val     = float('inf')
