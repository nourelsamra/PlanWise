    tr_loss  = train_step()
    val_loss = eval_step(val_mask_tf)

    train_losses.append(float(tr_loss))
    val_losses.append(float(val_loss))

    if float(val_loss) < best_val:
        best_val     = float(val_loss)
        best_epoch   = epoch
        best_weights = [v.numpy() for v in model.trainable_variables]

    if epoch % 50 == 0:
        print(f"Epoch {epoch:03d} | Train: {tr_loss:.6f} | Val: {val_loss:.6f} | Best Val: {best_val:.6f} @ {best_epoch}")

