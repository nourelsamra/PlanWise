# Simple DFS cycle breaking
def dfs_break_cycles(G):
    visited   = set()
    rec_stack = set()
    edges_to_remove = []

    def dfs(node):
        visited.add(node)
        rec_stack.add(node)
        for nb in list(G.successors(node)):
            if nb not in visited:
                dfs(nb)
            elif nb in rec_stack:
                edges_to_remove.append((node, nb))
        rec_stack.discard(node)

    for node in list(G.nodes()):
        if node not in visited:
            dfs(node)

    for u, v in edges_to_remove:
        if G.has_edge(u, v):
            G.remove_edge(u, v)
    return edges_to_remove

removed = dfs_break_cycles(G)
